#!/bin/bash
# Generate test coverage report - Enhanced
# Implements: Quality First principle (70%+ coverage)

set -e

# Colors
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

MIN_COVERAGE=70

echo "‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ"
echo "üìä Enhanced Coverage Report Generator - ÿ®ÿµŸäÿ± MVP v2.0"
echo "‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ"
echo ""

# Run tests with coverage
echo -e "${BLUE}üß™ Running tests with coverage...${NC}"
flutter test --coverage --reporter expanded
echo ""

# Check if coverage file exists
if [ ! -f coverage/lcov.info ]; then
  echo -e "${YELLOW}‚ö†Ô∏è  No coverage file generated${NC}"
  exit 1
fi

# Install lcov if needed
if ! command -v lcov &> /dev/null; then
  echo -e "${BLUE}üì¶ Installing lcov...${NC}"
  sudo apt-get update -qq && sudo apt-get install -y lcov
  echo ""
fi

# Calculate coverage
echo -e "${BLUE}üìä Calculating coverage...${NC}"
COVERAGE=$(lcov --summary coverage/lcov.info 2>&1 | grep lines | awk '{print $2}' | sed 's/%//' || echo "0")

echo "üìà Total Coverage: ${COVERAGE}%"
echo "üéØ Target: ${MIN_COVERAGE}%"
echo ""

# Generate HTML report
echo -e "${BLUE}üìÑ Generating HTML report...${NC}"
genhtml coverage/lcov.info -o coverage/html --quiet

# Generate detailed report
echo -e "${BLUE}üìã Generating detailed report...${NC}"
lcov --list coverage/lcov.info > coverage/detailed_report.txt

echo ""
echo "‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ"
echo "üìä Coverage Report Summary"
echo "‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ"
echo ""
echo "üìà Coverage: ${COVERAGE}%"
echo "üéØ Target: ${MIN_COVERAGE}%"
echo ""

if (( $(echo "$COVERAGE >= $MIN_COVERAGE" | bc -l) )); then
  echo -e "${GREEN}‚úÖ Coverage meets Quality First principle (${MIN_COVERAGE}%+)${NC}"
else
  echo -e "${YELLOW}‚ö†Ô∏è  Coverage below target: ${COVERAGE}% < ${MIN_COVERAGE}%${NC}"
  echo -e "${YELLOW}üí° Quality First: Add more tests to reach ${MIN_COVERAGE}%+${NC}"
fi

echo ""
echo "üìÅ Reports generated:"
echo "   üìÑ HTML: coverage/html/index.html"
echo "   üìã Detailed: coverage/detailed_report.txt"
echo "   üìä LCOV: coverage/lcov.info"
echo ""
echo "üí° To view HTML report:"
echo "   xdg-open coverage/html/index.html  # Linux"
echo "   open coverage/html/index.html      # macOS"
echo ""
echo "‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ‚îÅ"
